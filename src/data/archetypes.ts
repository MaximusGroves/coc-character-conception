export type ArchetypeOption = {
  key: ArchetypeKey
  name: ArchetypeName
  core: Array<CharacteristicKey>
  occupations: Array<OccupationKey>
  bonusSkills: Array<SkillKey>
  talents: Array<string>
  traits: Array<string>
  description: string
}

const makeArchetypeOption = (
  key: ArchetypeKey,
  name: ArchetypeName,
  core: Array<CharacteristicKey>,
  occupations: Array<OccupationKey>,
  bonusSkills: Array<SkillKey>,
  traits: Array<string>,
  talents: Array<string>,
  description: string
): ArchetypeOption => {
  return {
    key,
    name,
    core,
    occupations,
    bonusSkills,
    traits,
    talents,
    description,
  }
}

